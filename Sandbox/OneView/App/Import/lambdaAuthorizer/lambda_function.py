    db_host = os.environ.get("DATABASE_HOST")
    user_id = os.environ.get("DATABASE_USERNAME")
    password = os.environ.get("DATABASE_PASSWORD")
    dbname = os.environ.get("DATABASE_NAME")
    select_query = 'Select Tokens.expiry_time from Tokens where Tokens.bearer_token = %s and Tokens.expiry_time >= %s'
    try:
        conn = pymysql.connect(host=db_host, user=user_id,
                               passwd=password, db=dbname, connect_timeout=50)
        cur = conn.cursor()
        cur.execute(select_query, (token, now))
        row_count = cur.rowcount
        if row_count > 0:
            token_is_valid = True
            records = cur.fetchall()
            row = records[0]
        else:
            token_is_valid = False
    except Exception as ex:
        logger.error("ERROR: Could not fetch the token")
        logger.error(ex)
    finally:
        cur.close()
        conn.close()
    return token_is_valid


